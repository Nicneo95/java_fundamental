// package is the name of this folder is always in lower case
package tutorial;
// VariableDataType is the name of this class always in camel case
// first chracter is always in upper case
public class VariableDataType{
    // entry point of the main program 
    public static void main(String[] args) {
        // primitive 
        int num1 = 5;
        double num2 = 2.0;
        boolean isWorking = true;
        // char have to use '' instead of ""
        char alphabet = 'c';
        // special data type
        String name = "hello world ";

        System.out.println(num1);
        System.out.println(num2);
        System.out.println(isWorking);
        System.out.println(alphabet);
        System.out.println(name);

    }
}